import java.io.*;

public class Iterate_over_Array {
  
    public static void main(String[] args) {
      
        // creating a 1 dimensional array
        int[] array = {1,2,3,4,5};
        
        // iterating over an array
        for(int i = 0; i < array.length; i ++)
        {
            System.out.println(array[i]);
        }
    }
}
